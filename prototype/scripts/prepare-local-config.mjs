import { readFileSync, writeFileSync } from 'node:fs';

const envFile = new URL('../.env.local', import.meta.url);
const outputFile = new URL('../src/app/primeui-license.local.ts', import.meta.url);
let license = process.env.PRIMEUI_LICENSE?.trim() ?? '';

if (!license) {
  try {
    const line = readFileSync(envFile, 'utf8')
      .split(/\r?\n/)
      .find((candidate) => candidate.trimStart().startsWith('PRIMEUI_LICENSE='));

    if (line) {
      license = line.slice(line.indexOf('=') + 1).trim();
      if (
        (license.startsWith('"') && license.endsWith('"')) ||
        (license.startsWith("'") && license.endsWith("'"))
      ) {
        license = license.slice(1, -1);
      }
    }
  } catch (error) {
    if (error?.code !== 'ENOENT') {
      throw error;
    }
  }
}

const generatedSource = `// Generated by scripts/prepare-local-config.mjs; do not commit.\nexport const primeUILicense = ${JSON.stringify(license)};\n`;
writeFileSync(outputFile, generatedSource, { mode: 0o600 });

console.log(`PrimeUI local licence: ${license ? 'configured' : 'not configured'}`);
